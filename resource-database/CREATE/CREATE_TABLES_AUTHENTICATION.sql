/*TABLES AUTHENTICATION AND PERMISIONS*/

--USUARIOS
CREATE TABLE CRM.USUARIO(
	ID NUMBER,
	USUARIO VARCHAR2(50),
	CONTRASENIA VARCHAR2(50),
	EMAIL VARCHAR2(30),
	FECHA_CREACION DATE,
	ESTADO VARCHAR2(4)
);


--ROLES
CREATE TABLE CRM.ROL(
	ID NUMBER,
	ROL VARCHAR2(50),
	DECRIPCION VARCHAR2(200),
	FECHA_CREACION DATE,
	ESTADO VARCHAR2(4)
);


--RUTAS
CREATE TABLE CRM.RUTA(
	ID NUMBER,
	DECRIPCION VARCHAR2(200),
	URL VARCHAR2(50),
	FECHA_CREACION DATE,
	ESTADO VARCHAR2(4)
);


--ACCESOS

CREATE TABLE CRM.ACCESO(
	ID NUMBER,
	DECRIPCION VARCHAR2(200),
	PATH_ROUTE VARCHAR2(50),
	FECHA_CREACION DATE,
	ESTADO VARCHAR2(4)
);


--USUARIO_ROL

CREATE TABLE CRM.PERMISOS(
	ID NUMBER,
	USUARIO NUMBER,
	ROL NUMBER,
	FECHA_CREACION DATE,
	ESTADO VARCHAR2(4)
);


--ROL_RUTA
CREATE TABLE CRM.ROL_RUTA(
	ID NUMBER,
	ROL NUMBER,
	RUTA NUMBER,
	FECHA_CREACION DATE,
	ESTADO VARCHAR2(4)
);


--RUTA_ACCESO
CREATE TABLE CRM.RUTA_ACCESO(
	ID NUMBER,
	RUTA NUMBER,
	ACCESO NUMBER,
	FECHA_CREACION DATE,
	ESTADO VARCHAR2(4)
);




